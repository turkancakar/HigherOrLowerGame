let ilk_hesap = Math.floor(Math.random() * accounts.length);
let ikinci_hesap = Math.floor(Math.random() * accounts.length);
let score = 0;

const currentAccountElement = document.getElementById('ilkHesapName');
const currentDescriptionElement = document.getElementById('ilkHesapAcıklama');
const currentFollowersElement = document.getElementById('ilkHesapTakipci');
const currentCountryElement = document.getElementById('ilkHesapUlke');

const secondAccountElement = document.getElementById('ikiciHesapName2');
const secondDescriptionElement = document.getElementById('ikiciHesapAcıklama2');
const secondFollowersElement = document.getElementById('ikiciHesapTakipci2');
const secondCountryElement = document.getElementById('ikiciHesapUlke2');

const resultMessage = document.getElementById('result-message');
const scoreCount = document.getElementById('score-count');

// İkinci hesabın takipçi sayısını gizleyen bir fonksiyon
function hideSecondAccountFollowers() {
    secondFollowersElement.textContent = "???";
}

// Bir hesabın detaylarını ekrana yazdırır
function displayAccount(accountIndex, elementName, elementDescription, elementFollowers, elementCountry, showFollowers = true) {
    const account = accounts[accountIndex];
    elementName.textContent = account.name;
    elementDescription.textContent = account.description;
    elementCountry.textContent = account.country;
    elementFollowers.textContent = showFollowers ? account.followers.toLocaleString() : "???";
}

// Yeni ikinci hesap seçimi için bir fonksiyon
function generateSecondAccount() {
    ikinci_hesap = Math.floor(Math.random() * accounts.length);

    // İlk hesapla aynı olmamasını sağla
    while (ilk_hesap === ikinci_hesap) {
        ikinci_hesap = Math.floor(Math.random() * accounts.length);
    }

    // Yeni ikinci hesabı ekrana yazdır
    displayAccount(ikinci_hesap, secondAccountElement, secondDescriptionElement, secondFollowersElement, secondCountryElement, false);
}

// Doğru tahminde hesapları yer değiştirir
function swapAccounts() {
    ilk_hesap = ikinci_hesap; // İkinci hesap, birinci hesaba geçer
    displayAccount(ilk_hesap, currentAccountElement, currentDescriptionElement, currentFollowersElement, currentCountryElement); // İlk hesabı güncelle
    generateSecondAccount(); // Yeni ikinci hesabı oluştur
}

// Tahmin işleyici
function handleGuess(guess) {
    const currentAccount = accounts[ilk_hesap];
    const secondAccount = accounts[ikinci_hesap];

    const correct = guess === 'higher' ? secondAccount.followers > currentAccount.followers : secondAccount.followers < currentAccount.followers;

    if (correct) {
        score++;
        scoreCount.textContent = score;

        // 2 saniye bekleyip hesapları değiştir
        setTimeout(() => {
            swapAccounts();
        }, 2000);
    } else {
        // Yanlış tahmin -> Oyun sonu
        Swal.fire({
            title: 'Oyun Bitti!',
            text: `Skorun ${score}`,
            icon: 'error',
            confirmButtonText: 'Tekrar Oyna'
        }).then(() => {
            // Oyunu sıfırla
            score = 0;
            scoreCount.textContent = score;
            generateNewGame();
        });
    }

    // İkinci hesabın takipçi sayısını göster
    secondFollowersElement.textContent = secondAccount.followers.toLocaleString();
}

// Yeni bir oyun başlatır
function generateNewGame() {
    ilk_hesap = Math.floor(Math.random() * accounts.length);
    generateSecondAccount();
    displayAccount(ilk_hesap, currentAccountElement, currentDescriptionElement, currentFollowersElement, currentCountryElement);
}

// Oyunu başlat
generateNewGame();

// Butonlara tıklama olaylarını bağla
document.getElementById('higher-btn').addEventListener('click', () => handleGuess('higher'));
document.getElementById('lower-btn').addEventListener('click', () => handleGuess('lower'));
